# The factorial of a number is the product of all the integers 
# from 1 to that number

factorial_of_5 = 1 * 2 * 3 * 4 * 5
factorial_of_6 = factorial_of_5 * 6
factorial_of_7 = factorial_of_6 * 7
factorial_of_8 = factorial_of_7 * 8

puts "The factorial of 5 is " + factorial_of_5.to_s
puts "The factorial of 6 is " + factorial_of_6.to_s
puts "The factorial of 7 is " + factorial_of_7.to_s
puts "The factorial of 8 is " + factorial_of_8.to_s